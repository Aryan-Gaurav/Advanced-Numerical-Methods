#include<bits/stdc++.h>
using namespace std;
double f1(double x,double y)
{
    return x*x+y*y;
}
double f2(double x,double y)
{
    return x-y*y;
}
double f3(double x,double y)
{
    return sqrt(x+y);
}
void solver(double x0,double xn,double y0,double h,function<double(double,double)> f)
{
    double k1,k2,k3,k4,k;
    while(fabs(x0-xn)>1e-6) //this breaks as soon as x0==xn 
    {
        k1=h*f(x0,y0);
        k2=h*f(x0+h/2,y0+k1/2);
        k3=h*f(x0+h/2,y0+k2/2);
        k4=h*f(x0+h,y0+k3);
        k=(k1+2*k2+2*k3+k4)/6.0;
        printf("f(%.3lf)=%.6lf\n",x0,y0);
        y0=y0+k;
        x0=x0+h;
    }
    printf("f(%.3lf)=%.6lf\n",x0,y0);
    printf("-----------------------------\n");
}
int main()
{
    solver(1,1.1,2.3,0.025,f1);
    solver(1,1.2,2.3,0.025,f1);
    solver(0,0.1,1,0.025,f2);
    solver(0.4,0.8,0.41,0.025,f3);
    return 0;
}
